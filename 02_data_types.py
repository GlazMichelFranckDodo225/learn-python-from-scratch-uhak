# Python supports several built-in data types:
# Integer (int): Whole numbers, e.g., 5, -10, 0
# Float (float): Decimal numbers, e.g., 3.14, -0.001, 2.0
# String (str): Textual data, e.g., "Hello, World!", 'Python'
# Boolean (bool): Represents True or False values
# List (list): Ordered collection of items, e.g., [1, 2, 3], 
#   ['apple', 'banana', 'cherry']
# Tuple (tuple): Immutable ordered collection of items, e.g., (1, 2, 3), 
#   ('apple', 'banana', 'cherry')
# Dictionary (dict): Unordered collection of key-value pairs, e.g., 
# {'name': 'Alice', 'age': 30} 
# Set (set): Unordered collection of unique items, e.g., {1, 2, 3}, 
#   {'apple', 'banana', 'cherry'}

age = 3
print(age)
print(type(age))

name = "Harry"
print(name)
print(type(name))

price = 4.55
print(price)
print(type(price))

is_completed = False
print(is_completed)
print(type(is_completed))