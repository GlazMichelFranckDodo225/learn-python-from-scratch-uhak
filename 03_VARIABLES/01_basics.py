"""
In Python, variables are used to store information 
that can be referenced and manipulated in a program.
A variable is created the moment you first assign a 
value to it using the assignment operator (=).
"""
age = 34 # Integer
name = "Harry" # String
price = 4.55 # Float
# Rules of defining variables in Python:
# 1. Variable names must start with a letter or an underscore (_).
# 2. Variable names can only contain letters, numbers, and underscores.
# 3. Variable names are case-sensitive (age and Age are different variables).
# 4. Variable names cannot be a reserved keyword in Python (like if, else, while, etc.).
# 34age = 34 # Invalid variable name because it starts with a number
# a$$ge = 34 # Invalid variable name because it contains special characters
_age = 34 # Valid variable name
_nice_variable = 34 # Valid variable name
a_b_c_7 = "John Doe" # Valid variable name